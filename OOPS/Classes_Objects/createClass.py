class Car():
    #Methods / properties of class Car 
    def Start(self):
        print("CAR IS STARTING........")
    
    def Stop(self):
        print("CAR IS STOPPING.........")

# Objects of class Car
car1 = Car()
car2 = Car()

# Calling the  Objects here 

car1.Start()
car1.Stop()

car2.Start()
car2.Stop()



